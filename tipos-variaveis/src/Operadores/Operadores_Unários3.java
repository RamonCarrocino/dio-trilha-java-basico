package Operadores;
public class Operadores_Unários3 {
    public static void main(String[] args) {
        boolean variavel = true;

        System.out.println (!variavel); //"negação da expressção Boolean."

        System.out.println (variavel);

        variavel = !variavel;  //"dando novo valor a variavel"

        System.out.println (variavel);
    }
    
}
