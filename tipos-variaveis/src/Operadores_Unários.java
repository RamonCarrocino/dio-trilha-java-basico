
public class Operadores_Unários {

    
    public static void main(String[] args) {
        int numero = 5;
        
        numero = -numero;
        
        System.out.println (numero);

        numero = numero *-1; //"para tornar o número positivo é preciso realizar essa operação."
    
        System.out.println (numero);
    }
    
    
}
